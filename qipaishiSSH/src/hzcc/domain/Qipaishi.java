package hzcc.domain;

public class Qipaishi {
	private Long id;
	private String qipaishiname;
	private String type;
	private String size;
	private String price;
	private String state;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	
	public String getQipaishiname() {
		return qipaishiname;
	}
	public void setQipaishiname(String qipaishiname) {
		this.qipaishiname = qipaishiname;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	

}
